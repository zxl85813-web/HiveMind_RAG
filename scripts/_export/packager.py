"""Packager — turn a Blueprint into a self-contained delivery directory / ZIP.

Pipeline (each step emits a PackagerProgress event):
    1. validate    — schema is already valid; cross-check asset references
    2. layout      — create output dir tree
    3. copy_backend — copy backend/ minus tests/debug junk
    4. copy_frontend — copy frontend/dist if it exists, else mark as "build needed"
    5. copy_assets  — skills / mcp-servers / extra_paths
    6. write_env    — generate .env.example
    7. write_compose — generate docker-compose.yml from prod template
    8. write_lock   — freeze blueprint into blueprint.lock.yaml
    9. write_readme — generate README_DEPLOY.md
   10. zip          — optional — bundle into <name>-<version>.zip

The packager is intentionally pure file-system: no network, no docker calls.
docker image bundling is left for a future ``--format docker`` mode.
"""

from __future__ import annotations

import json
import shutil
import zipfile
from collections.abc import Callable, Iterable
from dataclasses import dataclass, field
from pathlib import Path
from typing import Literal

import yaml

from .assets import REPO_ROOT, scan_assets
from .schema import (
    Blueprint,
    PlatformModeEnum,
    UIModeEnum,
)

ProgressStatus = Literal["start", "ok", "skip", "warn", "error"]


@dataclass
class PackagerProgress:
    step: str
    status: ProgressStatus
    detail: str = ""


@dataclass
class PackagerResult:
    output_dir: Path
    zip_path: Path | None = None
    files_written: int = 0
    bytes_written: int = 0
    warnings: list[str] = field(default_factory=list)


# ── File-copy filters ───────────────────────────────────────────────────────
# Pattern matching is intentionally simple: substring match on the relative path.
# This is faster than fnmatch and good enough for the directory layouts here.

_BACKEND_EXCLUDE_DIRS = {
    "__pycache__",
    "__pycache__old",
    ".pytest_cache",
    ".mypy_cache",
    "htmlcov",
    "tests",
    "benchmarks",
    "data",
    "start_data",
    "exports",
    "logs",
    "storage",
    "hivemind_rag.egg-info",
}

_BACKEND_EXCLUDE_FILE_PREFIXES = (
    "test_",
    "debug_",
    "eval_",
    "check_",
    "dump_",
    "list_routes",
    "mcp_test",
    "mcp_swarm_test",
    "dummy_mcp_server",
    "temp.",
    "out.",
    "trace.",
)

_BACKEND_EXCLUDE_SUFFIXES = (".pyc", ".log", ".db", ".db-journal")


def _backend_filter(rel: Path) -> bool:
    """Return True to include the file, False to skip."""
    parts = rel.parts
    if any(part in _BACKEND_EXCLUDE_DIRS for part in parts):
        return False
    name = rel.name
    if name.startswith(_BACKEND_EXCLUDE_FILE_PREFIXES):
        return False
    if name.endswith(_BACKEND_EXCLUDE_SUFFIXES):
        return False
    return True


def _copy_tree(src: Path, dst: Path, predicate: Callable[[Path], bool]) -> tuple[int, int]:
    """Copy ``src`` → ``dst`` honouring ``predicate(rel_path)``. Returns (files, bytes)."""
    files = 0
    total_bytes = 0
    for entry in src.rglob("*"):
        if entry.is_dir():
            continue
        rel = entry.relative_to(src)
        if not predicate(rel):
            continue
        target = dst / rel
        target.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(entry, target)
        files += 1
        total_bytes += entry.stat().st_size
    return files, total_bytes


